// == means only value checking 
// === means value and type both checking.  ( it's recommended)

const first = 3
const second = "3"  
if (first == second) {
    console.log("condition is true");    
}
else{
    console.log("condition is false");
}
// === means value and type both checking.
const first1 = 3
const second2 = "3"  
if (first1 === second2) {
    console.log("condition is true");    
}
else{
    console.log("condition is false");
}